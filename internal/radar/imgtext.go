// drawCityLabels renders city name labels directly onto the image for inline
// image rendering modes where half-block text overlay isn't available.
func drawCityLabels(src image.Image, bb *BBox) image.Image {
	if bb == nil {
		return src
	}
	cities := citiesInBBox(*bb)
	if len(cities) == 0 {
		return src
	}

	bounds := src.Bounds()
	w, h := bounds.Dx(), bounds.Dy()

	// Copy to a mutable RGBA image.
	dst := image.NewRGBA(bounds)
	draw.Draw(dst, bounds, src, bounds.Min, draw.Src)

	lonSpan := bb.MaxLon - bb.MinLon
	latSpan := bb.MaxLat - bb.MinLat
	if lonSpan == 0 || latSpan == 0 {
		return src
	}

	white := color.RGBA{255, 255, 255, 255}
	outline := color.RGBA{0, 0, 0, 200}

	for _, c := range cities {
		px := int((c.Lon - bb.MinLon) / lonSpan * float64(w))
		py := int((bb.MaxLat - c.Lat) / latSpan * float64(h))
		if px < 0 || px >= w || py < 0 || py >= h {
			continue
		}

		// Draw a marker dot (5×5 filled circle).
		drawDot(dst, px, py, 3, white, outline)

		// Draw label text offset to the right of the dot.
		text := c.Name
		drawBitmapText(dst, px+6, py-4, text, white, outline)
	}

	return dst
}

// drawDot draws a small filled circle with outline at (cx, cy).
func drawDot(img *image.RGBA, cx, cy, r int, fill, border color.RGBA) {
	bounds := img.Bounds()
	for dy := -r - 1; dy <= r+1; dy++ {
		for dx := -r - 1; dx <= r+1; dx++ {
			x, y := cx+dx, cy+dy
			if x < bounds.Min.X || x >= bounds.Max.X || y < bounds.Min.Y || y >= bounds.Max.Y {
				continue
			}
			d2 := dx*dx + dy*dy
			if d2 <= r*r {
				img.Set(x, y, fill)
			} else if d2 <= (r+1)*(r+1) {
				img.Set(x, y, border)
			}
		}
	}
}

// drawBitmapText renders text using a built-in 5×7 bitmap font with a dark
// outline for readability against any background.
func drawBitmapText(img *image.RGBA, x, y int, text string, fg, outline color.RGBA) {
	bounds := img.Bounds()
	// Scale factor: on 1600×1600 images, use 2× for readability.
	scale := 2
	cx := x
	for _, ch := range text {
		glyph, ok := bitmapFont[ch]
		if !ok {
			glyph = bitmapFont['?']
		}
		// Draw outline (1 pixel border around each set pixel).
		for row := 0; row < 7; row++ {
			for col := 0; col < 5; col++ {
				if glyph[row]&(1<<(4-col)) != 0 {
					for ody := -1; ody <= 1; ody++ {
						for odx := -1; odx <= 1; odx++ {
							if odx == 0 && ody == 0 {
								continue
							}
							for sy := 0; sy < scale; sy++ {
								for sx := 0; sx < scale; sx++ {
									px := cx + (col*scale + sx + odx*scale)
									py := y + (row*scale + sy + ody*scale)
									if px >= bounds.Min.X && px < bounds.Max.X && py >= bounds.Min.Y && py < bounds.Max.Y {
										img.Set(px, py, outline)
									}
								}
							}
						}
					}
				}
			}
		}
		// Draw foreground on top.
		for row := 0; row < 7; row++ {
			for col := 0; col < 5; col++ {
				if glyph[row]&(1<<(4-col)) != 0 {
					for sy := 0; sy < scale; sy++ {
						for sx := 0; sx < scale; sx++ {
							px := cx + col*scale + sx
							py := y + row*scale + sy
							if px >= bounds.Min.X && px < bounds.Max.X && py >= bounds.Min.Y && py < bounds.Max.Y {
								img.Set(px, py, fg)
							}
						}
					}
				}
			}
		}
		cx += (5 + 1) * scale // 5 pixels per glyph + 1 pixel spacing, scaled
	}
}


// NearestStation returns the NEXRAD station closest to the given coordinates.
func NearestStation(lat, lon float64) Station {
	best := nexradStations[0]
	bestDist := haversine(lat, lon, best.Lat, best.Lon)
	for _, s := range nexradStations[1:] {
		d := haversine(lat, lon, s.Lat, s.Lon)
		if d < bestDist {
			best = s
			bestDist = d
		}
	}
	return best
}

// haversine returns the great-circle distance in km between two points.
func haversine(lat1, lon1, lat2, lon2 float64) float64 {
	const R = 6371.0 // Earth radius in km
	dLat := (lat2 - lat1) * math.Pi / 180
	dLon := (lon2 - lon1) * math.Pi / 180
	a := math.Sin(dLat/2)*math.Sin(dLat/2) +
		math.Cos(lat1*math.Pi/180)*math.Cos(lat2*math.Pi/180)*
			math.Sin(dLon/2)*math.Sin(dLon/2)
	return R * 2 * math.Atan2(math.Sqrt(a), math.Sqrt(1-a))
}
